// Get districts for a city
export const getDistrictsForCity = (city: string): string[] => {
  if (!city) return []
  return DISTRICTS_BY_CITY[city] || []
}

// Check if a city has predefined districts
export const hasPredefinedDistricts = (city: string): boolean => {
  return city in DISTRICTS_BY_CITY
}

// Get all unique districts across all cities
export const getAllDistricts = (): string[] => {
  const districts = new Set<string>()
  Object.values(DISTRICTS_BY_CITY).forEach(cityDistricts => {
    cityDistricts.forEach(district => districts.add(district))
  })
  return Array.from(districts).sort()
}



